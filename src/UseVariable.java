
public class UseVariable {

    public static void main(String[] args) {
        int number1;
        String str1;
        number1 = 1;
        str1 = "abc";
        System.out.println(number1);
        System.out.println(str1);
        int number2 = 100;
        String str2 = "XYZ";
        System.out.println(number2);
        System.out.println(str2);
        String str = "何らかのデータ";
        str =  "別の何か";
        System.out.println(str);
    }

}
